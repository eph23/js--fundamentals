"use strict";

/* 
In JavaScript, a "for loop" is a control flow statement that allows you to repeatedly execute a block of code. It's designed for situations where you know in advance how many times you want to run the code.
*/

for (let i = 1; i <= 10; i++) {
    console.log(`Lifting weights rep ${i} 🏋️‍♂️`);
}
